    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        // TODO add your handling code here:
        if(list1.getSelectedValue()== null){
            JOptionPane.showMessageDialog(null,"Chưa chọn địa điểm");
        }else{
            list2.setModel(model);
            if(temp2.equals(list1.getSelectedValue()))
               JOptionPane.showMessageDialog(null,"Địa điểm vừa chọn trước đó"); 
            else{
                temp.add(temp2=list1.getSelectedValue());
                model.addElement(list1.getSelectedValue());
            }
        }
    }//GEN-LAST:event_addbtnActionPerformed

    private void removebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removebtnActionPerformed
        // TODO add your handling code here:
        if(list2.getSelectedValue() == null){
            JOptionPane.showMessageDialog(null,"Chưa chọn địa điểm");
        }else{
            temp.remove(list2.getSelectedValue());
            model.removeElementAt(list2.getSelectedIndex());
        }
    }//GEN-LAST:event_removebtnActionPerformed

