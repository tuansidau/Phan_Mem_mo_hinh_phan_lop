    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String tb="";
        if(temp.size()==0){
            tb+="Danh sách địa điểm không được để trống\n";
        }
        if(tentxt.getText().equals("")){
            tb+="Tên tour không được để trống\n";
        }
        if(motatxt.getText().equals("")){
            tb+="Tên thành phố không được để trống\n";
        }
        if(new TourBLL().checkTen(tentxt.getText().toString())){
            tb+="Tên tour trùng\n";
        }
        if(!tb.equals("")){
             JOptionPane.showMessageDialog(null,tb);
        }else{
            boolean check = true;
            try{
                id2=Integer.parseInt(table.getValueAt(table.getRowCount()-1,0).toString())+1;
            }catch(Exception e){
                
            }
            if(!new TourBLL().them(new TourDTO(id2,tentxt.getText().toString(),
                    motatxt.getText().toString(),comboboxloai.getSelectedIndex()+1)))
                    check = false;
            else{
                int count=1;
                for(String s : temp){
                    if(!new ChitietTourBLL().them(new ChitietTourDTO(new ChitietTourBLL().docdsdetail().size()+1,
                            id2,count++,new DiadiemBLL().getId(s))))
                        check = false;
                }
            }
            if(check == true){
                JOptionPane.showMessageDialog(null,"Vui long them gia");
                Object [] newrow = {id2,tentxt.getText().toString(),motatxt.getText().toString(),
                     comboboxloai.getSelectedItem(),loadDd()};
                DefaultTableModel model = (DefaultTableModel) table.getModel();
                model.addRow(newrow);
                table.setModel(model);
                new ThemGiaGUI().setVisible(true);
                this.setVisible(false);
            }else{
                JOptionPane.showMessageDialog(null,"Thất bại");
            }
            this.setVisible(false);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

