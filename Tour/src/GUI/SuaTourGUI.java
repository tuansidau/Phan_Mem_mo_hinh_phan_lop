    private void loadList(){
        DefaultListModel<String> model = new DefaultListModel<>();
        for(String s : new DiadiemBLL().docDsdd(comboboxtp.getSelectedItem().toString())){
            model.addElement(s);
        }
        list1.setModel(model);
    }
    
    private void loadList2(){
        String[] arr = table.getValueAt(table.getSelectedRow(),4).toString().split("-");
        for(String s : arr){
            temp.add(s);
            model.addElement(s);
        }
        list2.setModel(model);
    }
    private void loadTt(){
        tentxt.setText(table.getValueAt(table.getSelectedRow(),1).toString());
        motatxt.setText(table.getValueAt(table.getSelectedRow(),2).toString());
        DefaultComboBoxModel loaicbModel = (DefaultComboBoxModel) comboboxloai.getModel();
        loaicbModel.setSelectedItem(table.getValueAt(table.getSelectedRow(),3));
        comboboxloai.setModel(loaicbModel);
    }
    private String loadDd(){
        String kq="";
        for(String s : temp){
            kq+=s+"-";
        }
        return kq.substring(0,kq.length()-1);
    }
    
