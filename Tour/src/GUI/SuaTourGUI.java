    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String tb="";
        if(temp.size()==0){
            tb+="Danh sách địa điểm không được để trống\n";
        }
        if(tentxt.getText().equals("")){
            tb+="Tên tour không được để trống\n";
        }
        if(motatxt.getText().equals("")){
            tb+="Tên thành phố không được để trống\n";
        }
        if(new TourBLL().checkTen(tentxt.getText())){
            tb+="Tên thành phố trùng\n";
        }
        if(!tb.equals("")){
             JOptionPane.showMessageDialog(null,tb);
        }else{
            TourBLL tourbll = new TourBLL();
            ChitietTourBLL detailbll = new ChitietTourBLL();
            DiadiemBLL ddbll = new DiadiemBLL();
            boolean check = true;
            if(!tourbll.sua(new TourDTO(Integer.parseInt(table.getValueAt(table.getSelectedRow(),0).toString()),
                    tentxt.getText().toString(),motatxt.getText().toString(),comboboxloai.getSelectedIndex()+1)))
                    check = false;
            int count=1;
            detailbll.xoa(Integer.parseInt(table.getValueAt(table.getSelectedRow(),0).toString()));
            for(String s : temp){
                if(!detailbll.them(new ChitietTourDTO(detailbll.docdsdetail().size()+1,
                        Integer.parseInt(table.getValueAt(table.getSelectedRow(),0).toString()),
                        count++,ddbll.getId(s))))
                    check = false;
            }
            if(check == true){
                JOptionPane.showMessageDialog(null,"Thành công");
                DefaultTableModel model = (DefaultTableModel) table.getModel();
                model.setValueAt(tentxt.getText().toString(),table.getSelectedRow(),1);
                model.setValueAt(motatxt.getText().toString(),table.getSelectedRow() ,2);
                model.setValueAt(comboboxloai.getSelectedItem().toString(),table.getSelectedRow() , 3);
                model.setValueAt(loadDd(),table.getSelectedRow() , 4);
                table.setModel(model);
                
                this.setVisible(false);
            }else{
                JOptionPane.showMessageDialog(null,"Thất bại");
            }
            this.setVisible(false);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

