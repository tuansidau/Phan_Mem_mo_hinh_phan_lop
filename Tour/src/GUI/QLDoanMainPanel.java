    public void docDoan()
    {
        try
        {
            int rowCount = model.getRowCount();//remove all row
            for (int i = rowCount - 1; i >= 0; i--) 
            {
                model.removeRow(i);
            }
            for(DoanDTO nv : DoanBLL.sumArrDoan)
            {
                Vector row = new Vector();
                row.add(nv.getDoan_id());
                row.add(nv.getDoan_ten());
                row.add(nv.getDoan_ngaydi());
                row.add(nv.getDoan_ngayve());
                row.add(nv.getDoan_chitiet());                
                row.add(nv.formatTinhtrang());                
                
                model.addRow(row);
            }
            table.setModel(model);
        
        } catch(Exception e) {
            System.out.println("Lỗi đọc đoàn GUI");
        }
    }
    
    public void search()
    {
        int rowCount = model.getRowCount();//remove all row
        for (int i = rowCount - 1; i >= 0; i--) 
        {
            model.removeRow(i);
        }
        for(DoanDTO nv : bus.search(searchTxt.getText().toUpperCase(), combobox.getSelectedIndex()))
        {
            Vector row = new Vector();
            row.add(nv.getDoan_id());
            row.add(nv.getDoan_ten());
            row.add(nv.getDoan_ngaydi());
            row.add(nv.getDoan_ngayve());
            row.add(nv.getDoan_chitiet());
            row.add(nv.formatTinhtrang());

            model.addRow(row);
        }
        table.setModel(model);
    }
