    private void nhapchiphiBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nhapchiphiBtnActionPerformed
        // TODO add your handling code here:
        int i = table.getSelectedRow();
        if(table.getSelectionModel().isSelectionEmpty())
        {
            JOptionPane.showMessageDialog(null, "Chưa chọn đoàn cần nhập chi phí");
        } else if(table.getValueAt(i, 5).toString().equals("Đã nhập chi phí"))
        {
            JOptionPane.showMessageDialog(null, "Đoàn này đã nhập chi phí. Hãy chọn sửa chi phí");
        } else {
            AddChiphiDoanPanel.currentDoan_id = Integer.parseInt(table.getValueAt(i, 0).toString());
            AddChiphiDoanPanel.currentDoan_ten = table.getValueAt(i, 1).toString();
            OverrallFrame.changeMainInfo(14);
        }
    }//GEN-LAST:event_nhapchiphiBtnActionPerformed

